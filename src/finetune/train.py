    reply_id = tokenizer.encode("<|reply|>", add_special_tokens=False)[0] if "<|reply|>" in tokenizer.get_vocab() else 2
    end_id = tokenizer.encode("<|end|>", add_special_tokens=False)[0] if "<|end|>" in tokenizer.get_vocab() else 3
    lb_id = tokenizer.encode("<|linebreak|>", add_special_tokens=False)[0] if "<|linebreak|>" in tokenizer.get_vocab() else 9
    start_id = tokenizer.encode("<|start|>", add_special_tokens=False)[0] if "<|start|>" in tokenizer.get_vocab() else 1
    
    # ── Identify control token IDs (prompt-only tags → reduced loss weight) ──
    # Qwen base vocab (0 to original_vocab_size-1) = content → 1.0×
    # Our added special tokens = control → 0.3× (except <|end|> and <|lb|>)
    # Syllable tokens = content → 1.0×
    original_vocab_size = model.config.vocab_size if hasattr(model.config, 'vocab_size') else 151936
    
    # Collect all control token IDs (our added special tokens, not syllables)
    control_ids = set()
    for token_str in special_tokens:
        tid = tokenizer.encode(token_str, add_special_tokens=False)
        if tid:
            control_ids.add(tid[0])
    
    generated_ids = {end_id, lb_id}  # only <|end|> and <|linebreak|> are generated at inference
    
    # Syllable tokens start after control tokens — they're content
    # Any ID >= original_vocab_size that's NOT in control_ids is a syllable token
    content_start = original_vocab_size  # for diversity loss filtering
    
    print(f"   Token IDs: pad={pad_id} reply={reply_id} end={end_id} lb={lb_id} start={start_id}")
    print(f"   Original Qwen vocab: {original_vocab_size:,} | Control tokens: {len(control_ids)} | Generated: {generated_ids}")
    print(f"   Content start ID: {content_start}")

    # ── Build P6+P8 loss weight tensor ──
    token_weight = build_token_weight_tensor(
        vocab_size, dev, generated_ids, control_ids, TAG_LOSS_WEIGHT, LINEBREAK_BONUS
    )
    print(f"   P6+P8: Content-weighted loss (tags ×{TAG_LOSS_WEIGHT}, <|end|> ×1.0, "
          f"<|lb|> ×{1.0+LINEBREAK_BONUS})")
    print(f"   P7: Diversity loss (weight={DIVERSITY_LOSS_WEIGHT}, "
          f"samples={DIVERSITY_SAMPLES}/step, warmup={DIVERSITY_WARMUP})")

    # ── Apply LoRA ──
    print(f"\n🎯  Applying LoRA (r={LORA_CONFIG['r']}, alpha={LORA_CONFIG['lora_alpha']})...")
    model = prepare_model_for_kbit_training(model)
    model.gradient_checkpointing_enable(gradient_checkpointing_kwargs={"use_reentrant": False})
    lora_config = LoraConfig(**LORA_CONFIG)
    model = get_peft_model(model, lora_config)
    model.print_trainable_parameters()

    # ── Load data ──
    print(f"\n📖  Corpus: {corpus_path}")
    with open(corpus_path, encoding="utf-8") as f:
        lines = [l.strip() for l in f if l.strip()]

    data = tokenize_with_qwen(lines, tokenizer)
    train_loader, val_loader = get_dataloaders(data, TC["block_size"], batch_size, 
                                                  num_workers=TC["num_workers"], val_frac=0.10)

    # ── Optimizer ──
    opt = torch.optim.AdamW(model.parameters(), lr=SC["lr"], betas=(0.9, 0.95),
                             weight_decay=0.01)

    # ── Resume ──
    start_step = 0
    if resume_from:
        from peft import PeftModel
        resume_path = Path(resume_from)
        if not resume_path.exists():
            resume_path = CHECKPOINT_DIR / resume_from
        print(f"\n📂  Resuming from: {resume_path}")
        model = PeftModel.from_pretrained(model, resume_path)
        ckpt = torch.load(str(resume_path / "training_state.pt"), map_location=dev, weights_only=False)
        opt.load_state_dict(ckpt["optimizer_state_dict"])
        start_step = ckpt.get("step", 0)
        SC["max_steps"] = max(max_steps, start_step + 1000)
        max_steps = SC["max_steps"]
        print(f"   Step: {start_step} | Adjusted max_steps: {max_steps}")

    # ── Training ──
    print(f"\n{'='*60}\n🔥  TRAINING (v5 FIXED with v4.2.3 improvements)\n{'='*60}\n")
    model.train()
    step, best_val, plateau_count = start_step, float("inf"), 0
    loss_sum, loss_cnt = 0.0, 0
    t0 = time.time()
    it = iter(train_loader)
    pbar = tqdm(total=TC["eval_interval"], desc=f"  Steps 0-{TC['eval_interval']}", unit="s", leave=False)
    micro_step = 0

    while step < max_steps:
        try:
            x, y = next(it)
        except StopIteration:
            it = iter(train_loader); x, y = next(it)
        x, y = x.to(dev), y.to(dev)

        lr = get_lr(step, TC["warmup_steps"], max_steps, SC["lr"], TC["min_lr"])
        for pg in opt.param_groups: pg["lr"] = lr

        # ── v4.2.3: Scheduled Sampling with control token protection ──
        use_teacher_prob = max(0.5, 1.0 - step / max_steps * 0.5)
        if use_teacher_prob < 1.0:
            with torch.no_grad():
                with torch.autocast(device_type=dev, dtype=torch.bfloat16):
                    out_sample = model(x)
                # out_sample.logits shape: (B, T, V)
                model_tokens = out_sample.logits.argmax(dim=-1)
            x_model = torch.cat([x[:, :1], model_tokens[:, :-1]], dim=1)
            mask = torch.rand(x.shape, device=dev) < use_teacher_prob
            # PROTECT control tokens: never replace them during scheduled sampling
            is_control = torch.zeros_like(x, dtype=torch.bool)
            for cid in control_ids:
                is_control = is_control | (x == cid)
            mask = mask | is_control
            x_input = torch.where(mask, x, x_model)
        else:
            x_input = x

        # ── Forward ──
        with torch.autocast(device_type=dev, dtype=torch.bfloat16):
            out = model(x_input)
            logits = out.logits  # (B, T, V)

            # ── P6+P8: Content-weighted cross-entropy loss ──
            loss = F.cross_entropy(
                logits.view(-1, vocab_size), y.view(-1),
                ignore_index=pad_id, weight=token_weight
            )

            # ── P7: N-gram diversity loss ──
            if step >= DIVERSITY_WARMUP:
                div_loss = compute_diversity_loss(
                    logits, x_input, dev,
                    DIVERSITY_WINDOW, DIVERSITY_SAMPLES, control_ids
                )
                loss = loss + DIVERSITY_LOSS_WEIGHT * div_loss
        
        loss = loss / grad_accum
        loss.backward()
        micro_step += 1

        if micro_step % grad_accum == 0:
            torch.nn.utils.clip_grad_norm_(model.parameters(), TC["grad_clip"])
            opt.step()
            opt.zero_grad()
            step += 1
            loss_sum += loss.item() * grad_accum
            loss_cnt += 1
            pbar.update(1)
            pbar.set_postfix({"loss": f"{loss.item()*grad_accum:.4f}"})

        if step > 0 and step % TC["eval_interval"] == 0 and micro_step % grad_accum == 0:
            train_loss = loss_sum / loss_cnt
            val_loss = evaluate(model, val_loader, dev)
            is_best = val_loss < best_val
            trend = "📉" if is_best else "➡️"
            best_val = min(best_val, val_loss)
            plateau_count = 0 if is_best else plateau_count + 1

            pbar.close()
            status = f"loss={train_loss:.4f} val={val_loss:.4f} {trend} lr={lr:.2e}"
            patience = SC.get("patience", 0)
            if patience > 0:
                status += f"  [{plateau_count}/{patience}]"
            elapsed = time.time() - t0
            eta = (elapsed / max(1, step)) * (max_steps - step)
            print(f"── Step {step:5d}/{max_steps} ({elapsed:.0f}s, ETA: {eta:.0f}s) ── {status}")
            loss_sum = 0.0; loss_cnt = 0

            if is_best:
                p = save_checkpoint(model, tokenizer, opt, step, val_loss, stage,
                                    CHECKPOINT_DIR / f"qwen_stage{stage}_best")
                print(f"   🏆  Best! val={val_loss:.4f} → {p}")

            if patience > 0 and plateau_count >= patience:
                print(f"   ⏹️  Plateau ({patience} evals) — stopping")
                break

            if step < max_steps:
                nxt = min(step + TC["eval_interval"], max_steps)
                pbar = tqdm(total=TC["eval_interval"], desc=f"  Steps {step}-{nxt}", unit="s", leave=False)

        if step > 0 and step % 2000 == 0:
            save_checkpoint(model, tokenizer, opt, step, float(loss.item()) * grad_accum, stage,
                            CHECKPOINT_DIR / f"qwen_stage{stage}_step{step}")

    pbar.close()

    # ── Final save + cloud upload ──
    final_path = save_checkpoint(model, tokenizer, opt, step, best_val, stage,
                                  CHECKPOINT_DIR / f"qwen_stage{stage}_final")

    elapsed = time.time() - t0
    print(f"\n{'='*60}")
    print(f"✅  Stage {stage} complete! {elapsed:.0f}s | Best val: {best_val:.4f}")
    print(f"{'='*60}\n")

    # ── Upload checkpoints to cloud storage ──
    print("☁️  Uploading checkpoints...")
    cloud_path = upload_checkpoints(CHECKPOINT_DIR, stage)
    print(f"📍  Cloud path: {cloud_path}")

    return str(final_path)


