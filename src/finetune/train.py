def build_token_weight_tensor(vocab_size, device, 
                               generated_ids: set, control_ids: set,
                               tag_weight: float, lb_bonus: float):
    """
    P6 + P8: Build per-class loss weight tensor.
    
    Control tokens (prompt-only tags like [RHYME:X], [TONE:...], <|reply|>)
    get reduced weight because the model never generates them at inference.
    <|end|> and <|linebreak|> ARE generated → full weight.
    <|linebreak|> gets extra bonus (P8).
    All content tokens (Qwen native vocab + syllable tokens) → 1.0×.
    
    Returns: tensor of shape (vocab_size,) for F.cross_entropy(weight=...)
    """
    token_weight = torch.ones(vocab_size, device=device)
    for cid in control_ids:
        if cid < vocab_size and cid not in generated_ids:
            token_weight[cid] = tag_weight
    # P8: linebreak bonus (applied even if in generated_ids)
    for lb_id in generated_ids:
        if lb_id < vocab_size:
            token_weight[lb_id] = 1.0 + lb_bonus
    return token_weight


def compute_diversity_loss(logits, input_ids, device, 
                            window, n_samples, control_ids: set):
    """
    P7: Penalize high logits on content tokens that appeared recently.
    
    Uses raw logit mean (no softmax) for memory efficiency.
    Samples random (batch, position) pairs to keep overhead ~5%.
    Excludes control tokens (prompt-only tags) and pad from diversity penalty.
    """
    B, T = input_ids.shape
    b_idx = torch.randint(0, B, (n_samples,), device=device)
    t_idx = torch.randint(1, T, (n_samples,), device=device)
    div_sum = torch.tensor(0.0, device=device)
    div_n = 0
    
    for i in range(n_samples):
        bi, ti = b_idx[i].item(), t_idx[i].item()
        start_t = max(0, ti - window)
        recent = input_ids[bi, start_t:ti]
        # Exclude control tokens from diversity tracking
        content_mask = torch.ones(len(recent), dtype=torch.bool, device=device)
        for cid in control_ids:
            content_mask = content_mask & (recent != cid)
        content_mask = content_mask & (recent != 0)  # exclude pad
        if not content_mask.any():
            continue
        unique_ids = recent[content_mask].unique()
        div_sum = div_sum + logits[bi, ti, unique_ids].mean()
        div_n += 1
    
    if div_n > 0:
        return div_sum / div_n
    return torch.tensor(0.0, device=device)


