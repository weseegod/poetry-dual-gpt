def load_syllable_vocab(vocab_path):
    """Load syllable vocabulary from JSON file.
    Returns list of syllable strings sorted by frequency (most common first).
    """
    if not vocab_path.exists():
        print(f"⚠️  Syllable vocab not found: {vocab_path}")
        print("   Run: python src/finetune/preprocess_syllables.py")
        return []
    
    with open(vocab_path, encoding='utf-8') as f:
        vocab = json.load(f)  # [[syllable, count], ...]
    
    syllables = [s for s, c in vocab]
    print(f"📖  Loaded {len(syllables):,} syllable candidates from {vocab_path}")
    return syllables


def add_syllable_tokens(tokenizer, syllable_vocab, max_new=5000):
    """
    Add Vietnamese syllables as single tokens to Qwen's tokenizer.
    
    Only adds syllables that Qwen's BPE tokenizer would split into
    multiple tokens. Syllables already present as single tokens are skipped.
    
    Returns: (num_added, list_of_syllable_tokens, token_ids_map)
      token_ids_map: {syllable: (new_token_id, [sub_token_ids])} for smart init
    """
    if not syllable_vocab:
        return 0, [], {}
    
    tokens_to_add = []
    token_ids_map = {}  # syllable → (new_id, sub_token_ids_for_init)
    already_single = 0
    
    print(f"🔍  Checking {len(syllable_vocab):,} syllables against Qwen tokenizer...")
    
    check_limit = min(len(syllable_vocab), max_new * 3)
    
    for syl in syllable_vocab[:check_limit]:
        if len(tokens_to_add) >= max_new:
            break
        
        token_ids = tokenizer.encode(" " + syl, add_special_tokens=False)
        
        if len(token_ids) == 1:
            already_single += 1
        elif len(token_ids) > 1:
            tokens_to_add.append(syl)
            token_ids_map[syl] = token_ids  # save sub-token IDs for smart init
    
    if already_single > 0:
        pct = already_single / min(len(syllable_vocab), check_limit) * 100
        print(f"   Already single tokens: {already_single:,} ({pct:.0f}%)")
    
    if not tokens_to_add:
        print(f"✅  All syllables are already single tokens in Qwen's vocab! No additions needed.")
        return 0, [], {}
    
    print(f"   Need to add: {len(tokens_to_add):,} syllables as special tokens")
    
    num_added = tokenizer.add_tokens(tokens_to_add, special_tokens=True)
    vocab_after = len(tokenizer)
    print(f"   Added: {num_added:,} tokens | Vocab now: {vocab_after:,}")
    print(f"   Examples: {tokens_to_add[:10]}")
    
    return num_added, tokens_to_add, token_ids_map


def init_syllable_embeddings(model, tokenizer, tokens_added, token_ids_map):
    """
    Smart initialization: set new syllable token embeddings to the average
    of their BPE sub-token embeddings from the original Qwen model.
    
    This prevents random-init overfitting: instead of training 5K embeddings
    from scratch, they start from a meaningful average of existing knowledge.
    """
    if not tokens_added or not token_ids_map:
        return
    
    embed_weight = model.get_input_embeddings().weight  # (vocab_size, hidden_dim)
    lm_head_weight = model.get_output_embeddings().weight  # (vocab_size, hidden_dim)
    
    initialized = 0
    for syl in tokens_added:
        new_id = tokenizer.encode(" " + syl, add_special_tokens=False)
        if len(new_id) != 1:
            continue  # should be single token now
        new_id = new_id[0]
        
        sub_ids = token_ids_map.get(syl, [])
        if not sub_ids or new_id >= embed_weight.shape[0]:
            continue
        
        # Average the sub-token embeddings (skip ids >= old vocab size)
        sub_embs = []
        for sid in sub_ids:
            if sid < embed_weight.shape[0]:
                sub_embs.append(embed_weight[sid])
        
        if sub_embs:
            avg_emb = torch.stack(sub_embs).mean(dim=0)
            # Add small noise to break symmetry for syllables sharing sub-tokens
            avg_emb = avg_emb + torch.randn_like(avg_emb) * 0.01
            embed_weight[new_id] = avg_emb
            lm_head_weight[new_id] = avg_emb
            initialized += 1
    
    print(f"   🧠  Smart-init: {initialized}/{len(tokens_added)} syllable embeddings "
          f"from BPE sub-token averages")


